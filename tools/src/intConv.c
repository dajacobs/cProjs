#include <stdio.h>

// Main function: examples of int conversions.
int main(void) {
	printf("%d\n", 455);
	printf("%i\n", 455);
	printf("%d\n", +455);
	printf("%hd\n", 32000);
	printf("%ld\n", 200000000L);
	printf("%o\n", 455);
	printf("%u\n", 455);
	printf("%u\n", -455);
	printf("%x\n", 455);
	printf("%X\n", 455);
}